from math import sqrt,pi,log10,gcd,cbrt,factorial,pow,e,lcm,exp,sin
import time
import datetime

start=time.time()
print(pi)
print(sqrt(14))
print(log10(1000))
print(factorial(5))
print(gcd(12,15))
print(cbrt(27))
print(pow(2,3))
print(e)
print(lcm(12,15))
print(exp(1))
print(sin(90))
end=time.time()
print("Execution Time:", end-start)
print(time.ctime())
datetime.time()
